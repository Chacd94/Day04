int		ft_iterative_factorial(int n)
{
	int f;
	int i;

	f = 1;
	i = 2;
	if (n < 0 || n > 12)
	{
		return (0);
	}
	else
	{
		while (i <= n)
		{
			f = f * i;
			i++;
		}
		return (f);
	}
}
