int		ft_iterative_power(int n, int p)
{
	int i;
	int cpy;

	cpy = n;
	if (p > 0)
	{
		i = 1;
		while (i < p)
		{
			n = n * cpy;
			i++;
		}
	}
	else if (p == 0)
	{
		n = 1;
	}
	else
	{
		n = 0;
	}
	return (n);
}
