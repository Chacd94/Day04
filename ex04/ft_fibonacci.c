#include <stdio.h>

int		ft_fibonacci(int i)
{
	int f;

	if (i < 0)
	{
		f = -1;
	}
	else if (i == 0)
	{
		f = 0;
	}
	else if (i == 1)
	{
		f = 1;
	}
	else
	{
		f = ft_fibonacci(i - 2) + ft_fibonacci(i - 1);
	}
	return (f);
}
