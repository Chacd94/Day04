int		ft_is_prime(int n)
{
	int i;
	int p;

	i = 2;
	p = 1;
	if (n <= 1)
	{
		return (0);
	}
	else
	{
		while (n % i != 0)
		{
			i = 2 * p + 1;
			p++;
		}
		if (i == n)
		{
			return (1);
		}
		else
		{
			return (0);
		}
	}
}
